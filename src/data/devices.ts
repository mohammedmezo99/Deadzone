function resolveImage(codename: string) {
    const imageKey = availableImages.has(codename) ? codename : imageFallbackByCodename[codename];
    return imageKey ? `/devices/${imageKey}.webp` : "/devices/fallback-device.svg";
}

function defineDevice(device: Omit<SupportedDevice, "image">): SupportedDevice {
    return {
        ...device,
        image: resolveImage(device.codename),
    };
}

